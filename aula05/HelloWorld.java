//Todo nome de classe começa com letra maiuscula
//com o nome do arquivo
class HelloWorld {
	//output (saida)
	public static void main(String[] args) {
		System.out.println("Hello Word");
	}
}

/*	
	class = declaração da classe
	HelloWorld = nome da classe
	
	############### METODO #################
	public static void main(String[] args) {
		System.out.println("Hello Word");
	}
	#########################################
	public static = modificadores de acesso do metodo
	void = não retorna nada
	main = nome do metodo
	(String[] args) = parametros/argumentos do metodo
	System.out.println = saida para o usuário
	javac Nome_Arquivo.java (compila)
	java Nome_Arquivo (executa)
*/